package mini_main;

public class Professor {
	private String proName;
	private String proSubject;
	private String proLab;
	
	public String getProName() {
		return proName;
	}
	public void setProName(String proName) {
		this.proName = proName;
	}
	public String getProSubject() {
		return proSubject;
	}
	public void setProSubject(String proSubject) {
		this.proSubject = proSubject;
	}
	public String getProLab() {
		return proLab;
	}
	public void setProLab(String proLab) {
		this.proLab = proLab;
	}
	
	
}
